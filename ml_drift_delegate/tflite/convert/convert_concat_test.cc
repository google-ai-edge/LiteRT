TEST_P(ConvertConcatTest, ConcatWithDuplicatedInput) {
  // Generate a model with a concat op.
  SingleOpInterpreterBuilder model(kTfLiteBuiltinConcatenation,
                                   /*version=*/1);
  model.AddInput(GetParam(), {1, 2, 3, 2});  // input 0
  model.AddInput(GetParam(), {1, 2, 3, 3});  // input 1
  // Input 0 is duplicated: 2 + 3 + 2 = 7
  model.AddOutput(GetParam(), {1, 2, 3, 7});
  TfLiteConcatenationParams* params =
      reinterpret_cast<TfLiteConcatenationParams*>(
          calloc(1, sizeof(TfLiteConcatenationParams)));
  model.SetParameters(params);

  // Build the interpreter and delegate it.
  // Note that we are passing input 0 twice, which will trigger the copy logic.
  std::unique_ptr<::tflite::Interpreter> interpreter = model.Build({0, 1, 0});
  ASSERT_TRUE(interpreter);
  ASSERT_EQ(interpreter->ModifyGraphWithDelegate(delegate_), kTfLiteOk);

  // Inspect the IrModel generated by the delegate.
  const ::ml_drift::ir::IrModel* ir_model = GetIrModel(delegate_);
  ASSERT_TRUE(ir_model);
  EXPECT_EQ(ir_model->inputs().size(), 2);
  EXPECT_EQ(ir_model->outputs().size(), 1);
  EXPECT_EQ(ir_model->ops().size(), 2);
  // concat op gets created first
  EXPECT_EQ(ir_model->op(0)->name, "concat");
  EXPECT_EQ(ir_model->op(1)->name, "copy");
  // Only two inputs to model, but we have three inputs to concat op
  EXPECT_EQ(ir_model->op(0)->inputs.size(), 3);
  // Also note that the 0 tensor will have two consumers
  EXPECT_EQ(ir_model->tensors().at(0)->consumers.size(), 2);

  // Sanity check inference.
  ASSERT_EQ(interpreter->AllocateTensors(), kTfLiteOk);
  ASSERT_EQ(interpreter->Invoke(), kTfLiteOk);
}

