TEST_P(ConvertConcatTest, ConcatWithConstantInput) {
  if (GetParam() != kTfLiteFloat32) {
    GTEST_SKIP() << "Skipping constant input test for non-float32 types for "
                    "simplicity of data generation.";
  }
  // Generate a model with a concat op.
  SingleOpInterpreterBuilder model(kTfLiteBuiltinConcatenation,
                                   /*version=*/1);
  model.AddInput(GetParam(), {1, 2, 3, 4});  // input 0
  std::vector<float> const_data = {1.0f,  2.0f,  3.0f,  4.0f,  5.0f,  6.0f,
                                   7.0f,  8.0f,  9.0f,  10.0f, 11.0f, 12.0f,
                                   13.0f, 14.0f, 15.0f, 16.0f, 17.0f, 18.0f,
                                   19.0f, 20.0f, 21.0f, 22.0f, 23.0f, 24.0f};
  std::vector<uint8_t> const_data_bytes(const_data.size() * sizeof(float));
  std::memcpy(const_data_bytes.data(), const_data.data(),
              const_data_bytes.size());

  model.AddConstInput(GetParam(), {1, 2, 3, 4}, const_data_bytes);  // input 1
  model.AddOutput(GetParam(), {1, 2, 3, 8});
  TfLiteConcatenationParams* params =
      reinterpret_cast<TfLiteConcatenationParams*>(
          calloc(1, sizeof(TfLiteConcatenationParams)));
  model.SetParameters(params);

  // Build the interpreter and delegate it.
  std::unique_ptr<::tflite::Interpreter> interpreter = model.Build();
  ASSERT_TRUE(interpreter);
  ASSERT_EQ(interpreter->ModifyGraphWithDelegate(delegate_), kTfLiteOk);

  // Inspect the IrModel generated by the delegate.
  const ::ml_drift::ir::IrModel* ir_model = GetIrModel(delegate_);
  ASSERT_TRUE(ir_model);
  // Constant input shouldn't be in model inputs.
  EXPECT_EQ(ir_model->inputs().size(), 1);
  EXPECT_EQ(ir_model->outputs().size(), 1);
  // We expect 2 ops: 1 for CONSTANT and 1 for CONCAT.
  ASSERT_EQ(ir_model->ops().size(), 2);
  // Constant op is added first during input parsing, then concat op.
  EXPECT_EQ(ir_model->op(0)->name, "const");
  EXPECT_EQ(ir_model->op(1)->name, "concat");

  const ::ml_drift::ir::IrOp* concat_op = ir_model->op(1);
  EXPECT_EQ(concat_op->inputs.size(), 2);
  EXPECT_EQ(concat_op->outputs.size(), 1);

  // Verify constant data.
  const ::ml_drift::ir::IrOp* const_op = ir_model->op(0);
  const ::ml_drift::ConstTensorAttributes* attr =
      std::any_cast<::ml_drift::ConstTensorAttributes>(&const_op->attr);
  ASSERT_TRUE(attr);
  const auto* t = std::get_if<::ml_drift::TensorFloat32>(&attr->tensor);
  ASSERT_TRUE(t);
  for (int i = 0; i < const_data.size(); ++i) {
    EXPECT_EQ(t->data[i], const_data[i]);
  }

  // Sanity check inference.
  ASSERT_EQ(interpreter->AllocateTensors(), kTfLiteOk);
  ASSERT_EQ(interpreter->Invoke(), kTfLiteOk);
}

