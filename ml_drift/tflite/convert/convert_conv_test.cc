TEST(ConvertConvTest, SpannedWeights) {
  IrModelBuilderOptions options;
  options.enable_spanned_weights = true;
  std::unique_ptr<TfLiteDelegate, void (*)(TfLiteDelegate*)> delegate(
      CreateStubDelegate(options), DeleteStubDelegate);

  SingleOpInterpreterBuilder model(kTfLiteBuiltinConv2d, /*version=*/1);
  model.AddInput(kTfLiteFloat32, {1, 2, 3, 4});   // input data
  model.AddInput(kTfLiteFloat32, {8, 1, 1, 4});   // weights
  model.AddOutput(kTfLiteFloat32, {1, 2, 3, 8});  // output
  TfLiteConvParams* params =
      reinterpret_cast<TfLiteConvParams*>(calloc(1, sizeof(TfLiteConvParams)));
  params->padding = kTfLitePaddingValid;
  params->stride_height = 1;
  params->stride_width = 1;
  params->activation = kTfLiteActNone;
  model.SetParameters(params);

  std::unique_ptr<::tflite::Interpreter> interpreter = model.Build();
  ASSERT_TRUE(interpreter);

  TfLiteTensor* weights_tensor = interpreter->tensor(interpreter->inputs()[1]);
  weights_tensor->allocation_type = kTfLiteMmapRo;
  std::vector<float> weights_data(8 * 1 * 1 * 4, 1.0f);
  weights_tensor->data.f = weights_data.data();
  weights_tensor->bytes = weights_data.size() * sizeof(float);

  ASSERT_EQ(interpreter->ModifyGraphWithDelegate(delegate.get()), kTfLiteOk);

  const ::ml_drift::ir::IrModel* ir_model = GetIrModel(delegate.get());
  ASSERT_TRUE(ir_model);
  ASSERT_EQ(ir_model->ops().size(), 1);

  const ::ml_drift::ir::IrOp* conv_op = ir_model->op(0);
  const auto* attr =
      std::any_cast<::ml_drift::Convolution2DAttributes>(&conv_op->attr);
  ASSERT_TRUE(attr);

  const auto& weights = std::get<
      ::ml_drift::Tensor<::ml_drift::OHWI, ::ml_drift::DataType::FLOAT32>>(
      attr->weights);
  EXPECT_TRUE(weights.data.empty());
  EXPECT_FALSE(weights.spanned_data.empty());
  EXPECT_EQ(weights.spanned_data.data(), weights_data.data());
}

