class ConvertBroadcastInDimTest : public ::testing::TestWithParam<TfLiteType> {
 protected:
  void SetUp() override {
    delegate_ = CreateStubDelegate();
    ASSERT_TRUE(delegate_);
  }

  void TearDown() override { DeleteStubDelegate(delegate_); }

  TfLiteDelegate* delegate_;
};

TEST_P(ConvertBroadcastInDimTest, LoadModelAndRunInference) {
  // Generate a model with a broadcast_in_dim op.
  SingleOpInterpreterBuilder model(kTfLiteBuiltinStablehloBroadcastInDim,
                                   /*version=*/1);
  model.AddInput(GetParam(), {1, 2, 3, 4});  // input data
  model.AddInput(kTfLiteInt32, {4});         // broadcast_dimensions
  model.AddOutput(GetParam(), {1, 2, 3, 4});
  std::vector<int> broadcast_dims_data = {0, 1, 2, 3};

  // Build the interpreter and delegate it.
  std::unique_ptr<::tflite::Interpreter> interpreter = model.Build();
  ASSERT_TRUE(interpreter);

  TfLiteTensor* const_tensor = interpreter->tensor(interpreter->inputs()[1]);
  const_tensor->allocation_type = kTfLiteMmapRo;
  const_tensor->data.i32 = broadcast_dims_data.data();

  ASSERT_EQ(interpreter->ModifyGraphWithDelegate(delegate_), kTfLiteOk);

  // Inspect the IrModel generated by the delegate.
  const ::ml_drift::ir::IrModel* ir_model = GetIrModel(delegate_);
  ASSERT_TRUE(ir_model);
  EXPECT_EQ(ir_model->inputs().size(), 1);
  EXPECT_EQ(ir_model->outputs().size(), 1);
  ASSERT_EQ(ir_model->ops().size(), 1);
  const ::ml_drift::ir::IrOp* transpose_op = ir_model->op(0);
  EXPECT_EQ(transpose_op->inputs.size(), 1);
  EXPECT_EQ(transpose_op->outputs.size(), 1);
  EXPECT_EQ(transpose_op->name, "transpose");
  const ::ml_drift::Transpose3DAttributes* attr =
      std::any_cast<::ml_drift::Transpose3DAttributes>(&transpose_op->attr);
  ASSERT_TRUE(attr);
  EXPECT_EQ(attr->perm, ::ml_drift::BHWDC(0, 1, 2, 3, 4));

  // Sanity check inference.
  ASSERT_EQ(interpreter->AllocateTensors(), kTfLiteOk);
  ASSERT_EQ(interpreter->Invoke(), kTfLiteOk);
}

