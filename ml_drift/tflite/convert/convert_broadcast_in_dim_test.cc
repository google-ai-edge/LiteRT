bool CanTile(const ::ml_drift::ir::IrTensor& in_tensor,
             const ::ml_drift::ir::IrTensor& out_tensor) {
  const ::ml_drift::BHWDC in_shape = in_tensor.desc.GetBHWDCShape();
  const ::ml_drift::BHWDC out_shape = out_tensor.desc.GetBHWDCShape();
  if (in_shape.b != 1 && in_shape.b != out_shape.b) return false;
  if (in_shape.h != 1 && in_shape.h != out_shape.h) return false;
  if (in_shape.w != 1 && in_shape.w != out_shape.w) return false;
  if (in_shape.d != 1 && in_shape.d != out_shape.d) return false;
  if (in_shape.c != 1 && in_shape.c != out_shape.c) return false;
  return true;
}

