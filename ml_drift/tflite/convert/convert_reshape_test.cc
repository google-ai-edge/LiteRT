class ConvertReshapeTest : public ::testing::TestWithParam<TfLiteType> {
 protected:
  void SetUp() override {
    delegate_ = CreateStubDelegate();
    ASSERT_TRUE(delegate_);
  }

  void TearDown() override { DeleteStubDelegate(delegate_); }

  TfLiteDelegate* delegate_;
};

TEST_P(ConvertReshapeTest, LoadModelAndRunInference) {
  // Generate a model with a reshape op.
  SingleOpInterpreterBuilder model(kTfLiteBuiltinReshape);
  model.AddInput(GetParam(), {4, 4});
  model.AddOutput(GetParam(), {2, 8});
  TfLiteReshapeParams* params = reinterpret_cast<TfLiteReshapeParams*>(
      calloc(1, sizeof(TfLiteReshapeParams)));
  params->num_dimensions = 2;
  params->shape[0] = 2;
  params->shape[1] = 8;
  model.SetParameters(params);

  // Build the interpreter and delegate it.
  std::unique_ptr<::tflite::Interpreter> interpreter = model.Build();
  ASSERT_TRUE(interpreter);
  ASSERT_EQ(interpreter->ModifyGraphWithDelegate(delegate_), kTfLiteOk);

  // Inspect the IrModel generated by the delegate.
  const ::ml_drift::ir::IrModel* ir_model = GetIrModel(delegate_);
  ASSERT_TRUE(ir_model);
  EXPECT_EQ(ir_model->inputs().size(), 1);
  EXPECT_EQ(ir_model->outputs().size(), 1);
  ASSERT_EQ(ir_model->ops().size(), 1);
  const ::ml_drift::ir::IrOp* ir_op =
      ir_model->op(ir_model->ops()[0]->id);
  EXPECT_EQ(ir_op->inputs.size(), 1);
  EXPECT_EQ(ir_op->outputs.size(), 1);
  EXPECT_EQ(ir_op->name, "reshape");
  const ::ml_drift::ReshapeAttributes* attr =
      std::any_cast<::ml_drift::ReshapeAttributes>(&ir_op->attr);
  ASSERT_TRUE(attr);
  EXPECT_EQ(attr->new_shape, ::ml_drift::BHWC(2, 1, 1, 8));

  // Sanity check inference.
  ASSERT_EQ(interpreter->AllocateTensors(), kTfLiteOk);
  ASSERT_EQ(interpreter->Invoke(), kTfLiteOk);
}

