TEST_F(ConvertReshapeTest, ReshapeWithOffsetTensorId) {
  // Generate a model with a reshape op, but add an unused input first to offset
  // the tensor IDs.
  SingleOpInterpreterBuilder model(kTfLiteBuiltinReshape);
  model.AddInput(kTfLiteFloat32, {1});      // Tensor 0: Dummy
  model.AddInput(kTfLiteFloat32, {4, 4});   // Tensor 1: Actual Input
  model.AddOutput(kTfLiteFloat32, {2, 8});  // Tensor 2: Actual Output
  TfLiteReshapeParams* params = reinterpret_cast<TfLiteReshapeParams*>(
      calloc(1, sizeof(TfLiteReshapeParams)));
  params->num_dimensions = 2;
  params->shape[0] = 2;
  params->shape[1] = 8;
  model.SetParameters(params);

  // Build the interpreter using only the actual input tensor.
  // This will result in TFLite tensor IDs [1] -> [2] for the delegated node.
  std::unique_ptr<::tflite::Interpreter> interpreter = model.Build({1});
  ASSERT_TRUE(interpreter);
  ASSERT_EQ(interpreter->ModifyGraphWithDelegate(delegate_), kTfLiteOk);

  // Inspect the IrModel generated by the delegate.
  const ::ml_drift::ir::IrModel* ir_model = GetIrModel(delegate_);
  ASSERT_TRUE(ir_model);
  ASSERT_EQ(ir_model->ops().size(), 1);
  const ::ml_drift::ir::IrOp* ir_op = ir_model->op(ir_model->ops()[0]->id);
  EXPECT_EQ(ir_op->name, "reshape");

  // Verify that attributes were correctly extracted from the output tensor.
  // Without the fix in convert_reshape.cc, this would have segfaulted because
  // it would try to access ir_model->tensor(2) instead of ir_model->tensor(1).
  const ::ml_drift::ReshapeAttributes* attr =
      std::any_cast<::ml_drift::ReshapeAttributes>(&ir_op->attr);
  ASSERT_TRUE(attr);
  EXPECT_EQ(attr->new_shape, ::ml_drift::BHWC(2, 1, 1, 8));
}

